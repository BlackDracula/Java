public class Calculator {
	public double sum(int num_1, int num_2) {
		return num_1+num_2;
	}
	public double multiply(int num_1, int num_2) {
		return num_1*num_2;
	}
	public double divide(int num_1, int num_2) {
		return num_1/num_2;
	}
	public double minus(int num_1, int num_2) {
		return num_1-num_2;
	}
}
